package com.senac.aula01.model;

/**
 *
 * @author fabiano.oss
 */
public class CarroNomeCor {
    
    private String nome;
    
    private String cor;

    public CarroNomeCor(String nome, String cor) {
        this.nome = nome;
        this.cor = cor;
    }
    
    
    

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getCor() {
        return cor;
    }

    public void setCor(String cor) {
        this.cor = cor;
    }
    
    
    
}
